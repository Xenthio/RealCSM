-- lua/realcsm/spread.lua
-- Spread sample point generation for shadow penumbra simulation.
-- Each method returns a table of Angle(x, y, 0) offsets (x=pitch offset, y=yaw offset)
-- within the unit disc scaled by csm_spread_radius. Index 1 is always Angle(0,0,0) (center).
--
-- Methods:
--   0 = Optimal (hardcoded ideal packing for N=1..24, falls back to Vogel beyond that)
--   1 = Vogel/golden-angle spiral (good for any N, uniform disc coverage)
--   2 = Legacy layer-based (original algorithm, kept for reference)

RealCSM = RealCSM or {}
RealCSM.Spread = {}

-- ── Helpers ───────────────────────────────────────────────────────────────────

local function pt(x, y) return Angle(x, y, 0) end

local function scalePoints(points, radius)
	local out = {}
	for _, p in ipairs(points) do
		out[#out + 1] = Angle(p.p * radius, p.y * radius, 0)
	end
	return out
end

-- ── Method 1: Vogel / golden-angle spiral ────────────────────────────────────
-- Places N points in a disc using the golden angle (~137.508°).
-- sqrt(i/N) radius gives uniform area distribution.
-- Works well for any N. No parameters needed.

local GOLDEN_ANGLE = 137.50776405003785 -- degrees; 360*(1 - 1/phi^2)

function RealCSM.Spread.Vogel(n, radius)
	local points = {}
	for i = 0, n - 1 do
		local r = radius * math.sqrt(i / n)
		local theta = math.rad(i * GOLDEN_ANGLE)
		points[#points + 1] = pt(math.cos(theta) * r, math.sin(theta) * r)
	end
	return points
end

-- ── Method 2: Legacy layer-based ─────────────────────────────────────────────
-- Original circle-packing-in-circle approximation using concentric rings.
-- Preserved for compatibility / comparison.

function RealCSM.Spread.Legacy(n, radius, layers, density, reserveMiddle, allocType)
	layers        = layers or 1
	density       = density or 0
	reserveMiddle = (reserveMiddle == nil) and true or reserveMiddle
	allocType     = allocType or 0

	-- Distribute samples across layers.
	local samplesPerLayer = {}
	for i = 1, layers do
		local v = n / layers
		samplesPerLayer[i] = (i == 1) and math.ceil(v) or math.floor(v)
	end

	-- Fixup rounding.
	local failsafe, sum = 0, 0
	repeat
		failsafe = failsafe + 1
		sum = 0
		for _, v in pairs(samplesPerLayer) do sum = sum + v end
		if     sum > n then samplesPerLayer[layers]   = samplesPerLayer[layers] - 1
		elseif sum < n then samplesPerLayer[failsafe] = samplesPerLayer[failsafe] + 1
		end
	until sum == n or failsafe == 2

	if allocType == 1 and layers > 2 then
		for k in pairs(samplesPerLayer) do
			if samplesPerLayer[k] > 2 then
				samplesPerLayer[k] = samplesPerLayer[k] - (k - 2)
			end
		end
	elseif layers > 1 and samplesPerLayer[layers] > 3 then
		samplesPerLayer[layers] = samplesPerLayer[layers] - 1
		samplesPerLayer[1]      = samplesPerLayer[1] + 1
	end

	if samplesPerLayer[layers] > 3 and layers > 1 and reserveMiddle then
		samplesPerLayer[layers] = samplesPerLayer[layers] - 1
	end

	local points = {}
	for i2 = 1, layers do
		local beans  = samplesPerLayer[i2]
		local i2r    = layers - (i2 - 1)
		local r      = ((i2r - ((-density) * (i2 - 1))) / layers) * radius
		for deg = 1, 360, 360 / beans do
			local theta = math.rad(deg)
			points[#points + 1] = pt(math.cos(theta) * r, math.sin(theta) * r)
		end
	end
	if layers > 1 and samplesPerLayer[layers] > 1 and reserveMiddle then
		points[#points + 1] = pt(0, 0)
	end

	return points
end

