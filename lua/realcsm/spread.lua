function RealCSM.Spread.Optimal(n, radius)
	local raw = OptimalRaw[n]
	if raw then
		local points = {}
		for _, v in ipairs(raw) do
			points[#points + 1] = pt(v[1] * radius, v[2] * radius)
		end
		return points
	end
	-- Fallback: Vogel spiral (good enough for N > 32)
	return RealCSM.Spread.Vogel(n, radius)
end

-- ── Dispatch ──────────────────────────────────────────────────────────────────

-- Returns a list of Angle offsets for the given method index, sample count, and radius.
--   method 0 = Optimal   (default)
--   method 1 = Vogel
--   method 2 = Legacy
function RealCSM.Spread.GetPoints(method, n, radius, legacyParams)
	if     method == 1 then return RealCSM.Spread.Vogel(n, radius)
	elseif method == 2 then
		local p = legacyParams or {}
		return RealCSM.Spread.Legacy(n, radius, p.layers, p.density, p.reserveMiddle, p.allocType)
	else
		return RealCSM.Spread.Optimal(n, radius)
	end
end
